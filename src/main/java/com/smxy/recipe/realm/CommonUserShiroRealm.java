package com.smxy.recipe.realm;

import com.smxy.recipe.dao.CommonUserDao;
import com.smxy.recipe.entity.CommonUser;
import org.apache.shiro.authc.AuthenticationException;
import org.apache.shiro.authc.AuthenticationInfo;
import org.apache.shiro.authc.AuthenticationToken;
import org.apache.shiro.authc.SimpleAuthenticationInfo;
import org.apache.shiro.authz.AuthorizationInfo;
import org.apache.shiro.realm.AuthorizingRealm;
import org.apache.shiro.subject.PrincipalCollection;
import org.apache.shiro.util.ByteSource;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.context.annotation.Lazy;

public class CommonUserShiroRealm extends AuthorizingRealm {

    @Autowired
    @Lazy
    private CommonUserDao commonUserDao;

    @Override
    protected AuthorizationInfo doGetAuthorizationInfo(PrincipalCollection principalCollection) {
        return null;
    }

    @Override
    protected AuthenticationInfo doGetAuthenticationInfo(AuthenticationToken authenticationToken) throws AuthenticationException {
        UserToken token = (UserToken) authenticationToken;
        String account = token.getUsername();
        CommonUser commonUser = commonUserDao.isUser(new CommonUser(account));
        if (commonUser == null) {
            return null;
        }
        Object credentials = commonUser.getFPassword();
        String realmName = getName();
        ByteSource credentialsSalt = ByteSource.Util.bytes(account);
        return new SimpleAuthenticationInfo(commonUser, credentials, credentialsSalt, realmName);
    }
}
